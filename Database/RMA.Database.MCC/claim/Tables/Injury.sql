CREATE TABLE [claim].[Injury] (
    [InjuryId]           INT          IDENTITY (1, 1) NOT NULL,
    [PhysicalDamageId]   INT          NOT NULL,
    [ICD10CodeId]        INT          NOT NULL,
    [BodySideAffectedId] INT          NULL,
