    [CreatedDate]        DATETIME     CONSTRAINT [DF_Injury_CreatedDate] DEFAULT (getdate()) NOT NULL,
    [ModifiedBy]         VARCHAR (50) NOT NULL,
    [ModifiedDate]       DATETIME     CONSTRAINT [DF_Injury_ModifiedDate] DEFAULT (getdate()) NOT NULL,
    CONSTRAINT [PK_Injury] PRIMARY KEY CLUSTERED ([InjuryId] ASC),
    CONSTRAINT [FK_Injury_BodySide] FOREIGN KEY ([BodySideAffectedId]) REFERENCES [common].[BodySideAffected] ([Id]),
    CONSTRAINT [FK_Injury_ICD10Code] FOREIGN KEY ([ICD10CodeId]) REFERENCES [medical].[ICD10Code] ([ICD10CodeID]),
    CONSTRAINT [FK_Injury_InjurySeverity] FOREIGN KEY ([InjurySeverityId]) REFERENCES [common].[InjurySeverity] ([Id]),
    CONSTRAINT [FK_Injury_InjuryStatus] FOREIGN KEY ([InjuryStatusId]) REFERENCES [common].[InjuryStatus] ([Id]),
    CONSTRAINT [FK_Injury_PhysicalDamage] FOREIGN KEY ([PhysicalDamageId]) REFERENCES [claim].[PhysicalDamage] ([PhysicalDamageId])
);








