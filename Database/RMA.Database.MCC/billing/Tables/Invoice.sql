CREATE TABLE [billing].[Invoice] (
    [InvoiceId]            INT             IDENTITY (1, 1) NOT NULL,
    [TenantId]             INT             CONSTRAINT [DF_Invoice_TenantId] DEFAULT ((1)) NOT NULL,
