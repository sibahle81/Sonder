CREATE TABLE [billing].[InvoiceAllocation] (
    [InvoiceAllocationId]     INT             IDENTITY (1, 1) NOT NULL,
    [TransactionId]           INT             NOT NULL,
    [CreatedDate]             DATETIME        NOT NULL,
    [CreatedBy]               VARCHAR (50)    NOT NULL,
    [ModifiedBy]              VARCHAR (50)    NOT NULL,
    [ModifiedDate]            DATETIME        NOT NULL,
    [InvoiceId]               INT             NULL,
    [Amount]                  DECIMAL (18, 2) NOT NULL,
    [ClaimRecoveryId]         INT             NULL,
    [IsDeleted]               BIT             CONSTRAINT [DF_InvoiceAllocation_IsDeleted] DEFAULT ((0)) NOT NULL,
    [DeletedReasonId]         INT             NULL,
    [LinkedTransactionId]     INT             NULL,
    [BillingAllocationTypeId] INT             NULL,
    [TransactionTypeLinkId]   INT             NULL,
    [ProductCategoryTypeId]   INT             NULL,
    CONSTRAINT [PK_InvoiceAllocation] PRIMARY KEY CLUSTERED ([InvoiceAllocationId] ASC),
    CONSTRAINT [FK_InvoiceAllocation_BillingAllocationType] FOREIGN KEY ([BillingAllocationTypeId]) REFERENCES [common].[BillingAllocationType] ([Id]),
    CONSTRAINT [FK_InvoiceAllocation_ClaimRecoveryInvoice] FOREIGN KEY ([ClaimRecoveryId]) REFERENCES [billing].[ClaimRecoveryInvoice] ([ClaimRecoveryInvoiceId]),
    CONSTRAINT [FK_InvoiceAllocation_DeletedReason] FOREIGN KEY ([DeletedReasonId]) REFERENCES [common].[DeletedReason] ([Id]),
    CONSTRAINT [FK_InvoiceAllocation_Invoice] FOREIGN KEY ([InvoiceId]) REFERENCES [billing].[Invoice] ([InvoiceId]),
    CONSTRAINT [FK_InvoiceAllocation_LinkedTransaction] FOREIGN KEY ([LinkedTransactionId]) REFERENCES [billing].[Transactions] ([TransactionId]),
    CONSTRAINT [FK_InvoiceAllocation_ProductCategoryType] FOREIGN KEY ([ProductCategoryTypeId]) REFERENCES [common].[ProductCategoryType] ([Id]),
    CONSTRAINT [FK_InvoiceAllocation_Transactions] FOREIGN KEY ([TransactionId]) REFERENCES [billing].[Transactions] ([TransactionId]),
    CONSTRAINT [FK_InvoiceAllocation_TransactionTypeLink] FOREIGN KEY ([TransactionTypeLinkId]) REFERENCES [billing].[TransactionTypeLink] ([Id])
);
















